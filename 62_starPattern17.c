#include<stdio.h>

int main()
{
    int i,j,n=9,k=0;
    for(i=1; i<=n;i++)    
    {
        for (j=1;j<=9;j++)
        {
            if(j<=5-k || j>=5+k)
                printf("*");
            else
                printf(" ");
        }
        printf("\n");

        if (i<5)
            k++;
        else
            k--;
    }
}